    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = -1;
        selectedIndex = tbQuanLySach.getSelectedRow();
        if (selectedIndex != -1) {
            Book book = bookList.get(selectedIndex);
            int selection = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa sách ?", "Delete", JOptionPane.YES_NO_OPTION);
            if (selection == JOptionPane.YES_OPTION) {
                Book.deleteBook(book.getMaSach());
                showAll();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Click vào sách muốn xóa ở bảng dưới và chọn XÓA");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên sách");
        if (!input.isEmpty()) {
            bookList = Book.findBook(input);
            model.setRowCount(0);
            bookList.forEach(bk -> {
                model.addRow(new Object[]{model.getRowCount() + 1,
                    bk.getMaSach(),
                    bk.getTenSach(),
                    bk.getSoLuong(),
                    bk.getDaMuon(),
                    bk.getConLai()
                });
            });
            tbQuanLySach.setModel(model);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // Chọn ra chỉ số của nó, ánh xạ qua bookList và sửa theo hàm Book.updateBook().

        tfMaSach.disable();
        int select = -1;
        select = tbQuanLySach.getSelectedRow();
        if (select != -1) {
            tfMaSach.setText(String.valueOf(bookList.get(select).getMaSach()));
            tfTenSach.setText(bookList.get(select).getTenSach());
            tfSoLuong.setText(String.valueOf(bookList.get(select).getSoLuong()));
        } else {
            JOptionPane.showMessageDialog(this, "Click vào sách muốn sửa ở bảng dưới và chọn LƯU THÔNG TIN SÁCH");
        }


    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            Book bk = new Book();
            bk.setTenSach(tfTenSach.getText());
            bk.setSoLuong(Integer.parseInt(tfSoLuong.getText()));

            Book.updateBook(tfMaSach.getText(), bk);
            showAll();
            tfMaSach.enable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Chưa có thông tin để lưu");
        }

    }//GEN-LAST:event_btnLuuActionPerformed

