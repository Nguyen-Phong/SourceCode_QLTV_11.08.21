    private void cbTinhTrangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTinhTrangActionPerformed
        // TODO add your handling code here:
        String select = (String) cbTinhTrang.getSelectedItem();
        if (select.equalsIgnoreCase("Toàn bộ")) {
            bookList = Book.findAll();
            model.setRowCount(0);
            bookList.forEach(bk -> {
                model.addRow(new Object[]{model.getRowCount() + 1,
                    bk.getMaSach(),
                    bk.getTenSach(),
                    bk.getSoLuong(),
                    bk.getDaMuon(),
                    bk.getConLai()});
            });
            tbQuanLySach.setModel(model);
        }
        if (select.equalsIgnoreCase("Đã được mượn")) {
            model.setRowCount(0);
            bookList = Book.findAllDaMuon();
            bookList.forEach(bk -> {
                model.addRow(new Object[]{model.getRowCount() + 1,
                    bk.getMaSach(),
                    bk.getTenSach(),
                    bk.getSoLuong(),
                    bk.getDaMuon(),
                    bk.getConLai()});
            });
            tbQuanLySach.setModel(model);
        }
        if (select.equalsIgnoreCase("Chưa được mượn")) {
            model.setRowCount(0);
            bookList = Book.findAllChuaMuon();
            bookList.forEach(bk -> {
                model.addRow(new Object[]{model.getRowCount() + 1,
                    bk.getMaSach(),
                    bk.getTenSach(),
                    bk.getSoLuong(),
                    bk.getDaMuon(),
                    bk.getConLai()});
            });
            tbQuanLySach.setModel(model);
        }
    }//GEN-LAST:event_cbTinhTrangActionPerformed

