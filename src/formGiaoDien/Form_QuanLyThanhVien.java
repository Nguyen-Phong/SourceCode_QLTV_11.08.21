    private void tfThemTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfThemTVActionPerformed
        // TODO add your handling code here:
        try {
            String ma = tfMaTV.getText();
            String ten = tfTenTV.getText();
            int tuoi = Integer.parseInt(tfTuoi.getText());
            String gender = (String) cbGioiTinh.getSelectedItem();
            String chucVu = tfChucVu.getText();
            String phone = tfPhone.getText();
            String diaChi = tfDiaChi.getText();

            Borrower br = new Borrower(ma, ten, tuoi, gender, chucVu, phone, diaChi);
            Borrower.create(br);
            showAllMember();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập đầy đủ thông tin trước khi thêm TV", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tfThemTVActionPerformed

    private void tfSuaTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfSuaTVActionPerformed
        // TODO add your handling code here:
        int index = -1;
        index = tbQuanLyThanhVien.getSelectedRow();
        if (index != -1) {
            tfMaTV.disable();
            chooseEachMem();
        } else {
            JOptionPane.showMessageDialog(this, "Click chuột vào người muốn sửa TT", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_tfSuaTVActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String ten = tfTenTV.getText();
        String tuoi = tfTuoi.getText();
        String gt = (String) cbGioiTinh.getSelectedItem();
        String cv = tfChucVu.getText();
        String phone = tfPhone.getText();
        String diaChi = tfDiaChi.getText();
        if(ten.isEmpty() || tuoi.isEmpty() || gt.isEmpty() || cv.isEmpty() || phone.isEmpty() || diaChi.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhập đầy đủ TT trước khi lưu", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int i = JOptionPane.showConfirmDialog(this, "Lưu TT ?", "Question", JOptionPane.YES_NO_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            Borrower br1 = new Borrower();
            br1.setTenThanhVien(tfTenTV.getText());
            br1.setTuoi(Integer.parseInt(tfTuoi.getText()));
            br1.setGioiTinh((String) cbGioiTinh.getSelectedItem());
            br1.setChucVu(tfChucVu.getText());
            br1.setPhone(tfPhone.getText());
            br1.setDiaChi(tfDiaChi.getText());

            Borrower.update(br1, tfMaTV.getText());
            showAllMember();
            tfMaTV.enable();
        }

    }//GEN-LAST:event_btnLuuActionPerformed

    private void tfXoaTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfXoaTVActionPerformed
        // TODO add your handling code here:
        int index = -1;
        index = tbQuanLyThanhVien.getSelectedRow();
        if (index != -1) {
            int input = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa?", "Question", JOptionPane.YES_NO_OPTION);
            if (input == JOptionPane.YES_OPTION) {
                Borrower.delete(brList.get(index).getMaThanhVien());
                showAllMember();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Click chuột vào người muốn xóa", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_tfXoaTVActionPerformed

    private void tfTimTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfTimTVActionPerformed
        // TODO add your handling code here:
        String option = JOptionPane.showInputDialog(this, "Enter ID's Borrower");
        if (option != null) {
            brList = Borrower.searchByID(option);
            model.setRowCount(0);
            for (Borrower br : brList) {
                model.addRow(new Object[]{model.getRowCount() + 1,
                    br.getMaThanhVien(), br.getTenThanhVien(),
                    br.getTuoi(), br.getGioiTinh(), br.getChucVu(),
                    br.getPhone(), br.getDiaChi()});
            }
            tbQuanLyThanhVien.setModel(model);
        }
    }//GEN-LAST:event_tfTimTVActionPerformed

