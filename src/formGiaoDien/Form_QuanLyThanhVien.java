    public void chooseEachMem() {
        int selectedIndex = tbQuanLyThanhVien.getSelectedRow();
        tfMaTV.setText(brList.get(selectedIndex).getMaThanhVien());
        tfTenTV.setText(brList.get(selectedIndex).getTenThanhVien());
        tfTuoi.setText(String.valueOf(brList.get(selectedIndex).getTuoi()));
        cbGioiTinh.setSelectedItem(brList.get(selectedIndex).getGioiTinh());
        tfChucVu.setText(brList.get(selectedIndex).getChucVu());
        tfPhone.setText(brList.get(selectedIndex).getPhone());
        tfDiaChi.setText(brList.get(selectedIndex).getDiaChi());
    }

    public void showAllMember() {
        brList = Borrower.findAllBorrower();
        model.setRowCount(0);
        for (Borrower br : brList) {
            model.addRow(new Object[]{model.getRowCount() + 1,
                br.getMaThanhVien(), br.getTenThanhVien(),
                br.getTuoi(), br.getGioiTinh(), br.getChucVu(),
                br.getPhone(), br.getDiaChi()});
        }
        tbQuanLyThanhVien.setModel(model);
    }

