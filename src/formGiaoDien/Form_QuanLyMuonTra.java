    public void showAll() {
        loanList = Loan.getAllLoan();
        model.setRowCount(0);
        loanList.forEach(ln -> {
            model.addRow(new Object[]{model.getRowCount() + 1,
                ln.getMaThanhVien(),
                ln.getTenThanhVien(),
                ln.getMaSach(),
                ln.getTenSach(),
                ln.getNgayMuon(), ln.getNgayTra(), ln.getPeriod()});
        });
        tbMuonTra.setModel(model);
    }

    public void bookStatistics() {
        bookList = Book.findAll();
        model1.setRowCount(0);
        bookList.forEach(bk -> {
            model1.addRow(new Object[]{model1.getRowCount() + 1,
                bk.getMaSach(), bk.getTenSach(),
                bk.getConLai()});
        });
        tbThongKeSach.setModel(model1);
    }

    public void memberStatistics() {
        borrowerList = Borrower.findAllBorrower();
        model2.setRowCount(0);
        borrowerList.forEach(br -> {
            model2.addRow(new Object[]{model2.getRowCount() + 1,
                br.getMaThanhVien(), br.getTenThanhVien(),
                br.getChucVu()});
        });
        tbThongKeThanhVien.setModel(model2);
    }

