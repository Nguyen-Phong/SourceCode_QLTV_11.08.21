    private void btnTraSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraSachActionPerformed
        // TODO add your handling code here:
        try {
            int select = tbMuonTra.getSelectedRow();
            Loan ln = loanList.get(select);
            String maTV = ln.getMaThanhVien();
            String maSach = ln.getMaSach();
            int tg = ln.getPeriod();
            int tienPhat = 0;
            if (maTV.startsWith("SV")) {
                if (tg > 90) {
                    int kd = JOptionPane.showConfirmDialog(this, "Trả sách?", "Question", JOptionPane.YES_NO_OPTION);
                    if (kd == JOptionPane.YES_OPTION) {
                        traSach(maSach, maTV);
                        tienPhat = (tg - 90) * 1000;
                        JOptionPane.showMessageDialog(this, "Bị phạt " + tienPhat + " vnd vì trả muộn " + (tg - 90) + " ngày!", "Wanring", JOptionPane.WARNING_MESSAGE);
                    }

                } else {
                    int kd = JOptionPane.showConfirmDialog(this, "Trả sách?", "Question", JOptionPane.YES_NO_OPTION);
                    if (kd == JOptionPane.YES_OPTION) {
                        traSach(maSach, maTV);
                    }
                }
            } else {
                if (tg > 365) {
                    int kd = JOptionPane.showConfirmDialog(this, "Trả sách?", "Question", JOptionPane.YES_NO_OPTION);
                    if (kd == JOptionPane.YES_OPTION) {
                        traSach(maSach, maTV);
                        tienPhat = (tg - 365) * 1000;
                        JOptionPane.showMessageDialog(this, "Bị phạt " + tienPhat + " vnd vì trả muộn " + (tg - 365) + " ngày!", "Wanring", JOptionPane.WARNING_MESSAGE);
                    }

                } else {
                    int kd = JOptionPane.showConfirmDialog(this, "Trả sách?", "Question", JOptionPane.YES_NO_OPTION);
                    if (kd == JOptionPane.YES_OPTION) {
                        traSach(maSach, maTV);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Chọn TT ở bảng trên rồi nhấn TRẢ SÁCH");
        }


    }//GEN-LAST:event_btnTraSachActionPerformed

