    private void btnMuonMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuonMoiActionPerformed

        int slIndex1 = -1, slIndex2 = -1;
        slIndex1 = tbThongKeSach.getSelectedRow();
        slIndex2 = tbThongKeThanhVien.getSelectedRow();

        while (slIndex1 == -1 || slIndex2 == -1) {
            JOptionPane.showMessageDialog(this, "Chọn thông tin ở bảng bên phải");
            return;
        }
        if (slIndex1 != -1 && slIndex2 != -1) {

            bookList = Book.findAll();
            borrowerList = Borrower.findAllBorrower();
            Book bk = bookList.get(slIndex1);
            tfMaSach.setText(bk.getMaSach());
            Borrower br = borrowerList.get(slIndex2);
            tfMaTV.setText(br.getMaThanhVien());
            JOptionPane.showMessageDialog(this, "Nhập ngày mượn / trả và chọn Save");
            tfNgayMuon.requestFocus();
        }

    }//GEN-LAST:event_btnMuonMoiActionPerformed

